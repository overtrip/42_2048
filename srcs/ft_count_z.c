#include "game_2048.h"

int		ft_count_z(int **tab)
{
	int j;
	int i;
	int	cmp;

	cmp = 0;
	j = -1;
	while (++j < 4)
	{
		i = -1;
		while (++i < 4)
			cmp += (tab[j][i] == 0) ? 1 : 0;
	}
	return (cmp);
}
